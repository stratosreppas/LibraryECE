
class User {

  final int id;
  final String name;
  final String surname;
  final String am;
  final String email;
  final String phone;
  final String penalty;
  final int property;

  User({
    required this.id,
    required this.name,
    required this.surname,
    required this.am,
    required this.email,
    required this.phone,
    required this.penalty,
    required this.property,
  });
}
